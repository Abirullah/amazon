function add(){
    let opendiv = document.getElementById("item");
         opendiv.addEventListener('click', function() {
            document.getElementById("item").classList.add("active");
        });

        document.getElementById("cross-btn").addEventListener('click',()=>{
            let opendivRef = document.getElementById("item")
            opendivRef.addEventListener('click', function() {
            document.getElementById("item").classList.remove("active");
        });

        });
}
